def temp_index(tempF):
    if tempF < 15:
        return 0
    elif tempF > 120:
        return 23
    else:
        return tempThresholds.index(round_to_nearest(tempF, 5))


def sol_index(sol_rad):
    if sol_rad < 100:
        return 0
    elif sol_rad > 1300:
        return 14
    else:
        return solarTherholds.index(round_to_nearest(sol_rad, 100))


def get_fuel_surf_temp(temp, sol_rad):
    tempF = temp * 1.8 + 32
    return fuelSurfacTemp[temp_index(tempF)][sol_index(sol_rad)]
