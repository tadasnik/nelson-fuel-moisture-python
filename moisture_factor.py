# Function to get the moisture correction factor
def get_moisture_correction_factor(init_moisture, eq_moist_content, fuel_temp, rel_hum):
    temp_ind = x_sorption_temp_ind(fuel_temp)
    hum_ind = x_sorption_hum_ind(rel_hum)
    x_sorption_prop = (
        f"{init_moisture}% Adsorption"
        if init_moisture < eq_moist_content
        else f"{init_moisture}% Desorption"
    )

    # Accessing the x_sorption table using the property, temperature index, and humidity index
    return x_sorption[x_sorption_prop][temp_ind][hum_ind]


# Function to get the temperature index
def x_sorption_temp_ind(temp):
    if temp < 30:
        return 0
    elif 30 <= temp <= 49:
        return 1
    elif 50 <= temp <= 69:
        return 2
    elif 70 <= temp <= 89:
        return 3
    elif 90 <= temp <= 99:
        return 4
    elif 100 <= temp <= 104:
        return 5
    elif 105 <= temp <= 109:
        return 6
    elif 110 <= temp <= 115:
        return 7
    else:
        return 8


# Function to get the humidity index
def x_sorption_hum_ind(hum):
    if hum < 5:
        return 0
    elif 5 <= hum <= 9:
        return 1
    elif 10 <= hum <= 14:
        return 2
    elif 15 <= hum <= 19:
        return 3
    elif 20 <= hum <= 24:
        return 4
    elif 25 <= hum <= 29:
        return 5
    elif 30 <= hum <= 34:
        return 6
    elif 35 <= hum <= 39:
        return 7
    elif 40 <= hum <= 44:
        return 8
    elif 45 <= hum <= 49:
        return 9
    elif 50 <= hum <= 54:
        return 10
    elif 55 <= hum <= 59:
        return 11
    elif 60 <= hum <= 64:
        return 12
    elif 65 <= hum <= 69:
        return 13
    elif 70 <= hum <= 74:
        return 14
    elif 75 <= hum <= 79:
        return 15
    elif 80 <= hum <= 84:
        return 16
    elif 85 <= hum <= 89:
        return 17
    elif 90 <= hum <= 94:
        return 18
    else:
        return 19


